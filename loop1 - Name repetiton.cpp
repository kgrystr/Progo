#include<iostream>
using namespace std;
int main (){

	//for loop 
	for (int counter = 0;counter < 5;counter++){
		cout << "Jimiel D. Balitayo" << endl;	
	}
	
	//do-while loop
    int counter2 = 0;	
	do {
		cout << "Jimiel Balitayo" << endl;
		counter2++;	
	} while (counter2 < 5); 
	
	//while loop
	int counter3 = 0;
	while (counter3 < 5 ){
		cout << "Jimiel Dinio Balitayo" << endl;
		counter3++;
	}
	
	cout << "\n\nProgram has ended." << endl;	
		
	return 0;
}
